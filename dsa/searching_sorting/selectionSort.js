
// ================================
// PASS 1 (Find minimum)
// ================================
// [ 5 | 3  8  2  1 ]
// Minimum = 1

// Swap 5 and 1

// [ 1 | 3  8  2  5 ]

// --------------------------------

// ================================
// PASS 2
// ================================
// [ 1 | 3  8  2  5 ]
// Minimum = 2

// Swap 3 and 2

// [ 1  2 | 8  3  5 ]

// --------------------------------

// ================================
// PASS 3
// ================================
// [ 1  2 | 8  3  5 ]
// Minimum = 3

// Swap 8 and 3

// [ 1  2  3 | 8  5 ]

// --------------------------------

// ================================
// PASS 4
// ================================
// [ 1  2  3 | 8  5 ]
// Minimum = 5

// Swap 8 and 5

// [ 1  2  3  5  8 ]

// --------------------------------

let a = [5, 3, 8, 2, 1];
let min, temp;

for (let i = 0; i < a.length; i++) {
  min = i;
  for (let j = i + 1; j < a.length; j++) {
    if (a[j] < a[min]) {
      min = j;
    }
  }

  temp = a[i];
  a[i] = a[min];
  a[min] = temp;
}

console.log(a);
