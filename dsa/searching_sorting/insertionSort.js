
// ================================
// PASS 1 (Insert 3)
// ================================
// Sorted | Unsorted
// [ 5 | 3  8  2  1 ]

// 3 < 5 → shift 5 right

// [ 5 | 5  8  2  1 ]
// [ 3 | 5  8  2  1 ]  ← insert 3

// --------------------------------

// ================================
// PASS 2 (Insert 8)
// ================================
// Sorted | Unsorted
// [ 3  5 | 8  2  1 ]

// 8 > 5 → no shift

// [ 3  5  8 | 2  1 ]

// --------------------------------

// ================================
// PASS 3 (Insert 2)
// ================================
// [ 3  5  8 | 2  1 ]

// 2 < 8 → shift
// 2 < 5 → shift
// 2 < 3 → shift

// [ 3  3  5  8 | 1 ]
// [ 2  3  5  8 | 1 ]  ← insert 2

// --------------------------------

// ================================
// PASS 4 (Insert 1)
// ================================
// [ 2  3  5  8 | 1 ]

// 1 < 8 → shift
// 1 < 5 → shift
// 1 < 3 → shift
// 1 < 2 → shift

// [ 2  2  3  5  8 ]
// [ 1  2  3  5  8 ]  ← sorted

function insertionSort(arr) {
  for (let i=1;i<arr.length;i++) {
    let key = arr[i], j = i-1;
    while (j>=0 && arr[j]>key) arr[j+1]=arr[j--];
    arr[j+1]=key;
  }
  return arr;
}